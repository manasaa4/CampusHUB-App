const teacherAttendance = async (req, res) => {
    const { status, date } = req.body;

    try {
        const teacher = await Teacher.findById(req.params.id);

        if (!teacher) {
            return res.send({ message: 'Teacher not found' });
        }

        const existingAttendance = teacher.attendance.find(
            (a) =>
                a.date.toDateString() === new Date(date).toDateString()
        );

        if (existingAttendance) {
            existingAttendance.status = status;
        } else {
            teacher.attendance.push({ date, status });
        }

        const result = await teacher.save();
        return res.send(result);
    } catch (error) {
        res.status(500).json(error)
    }
};

const teacherResetPasswordLink=async(req,res)=>{
    try {
        const user=await Teacher.findOne({email:req.body.email})

        if(!user){
            res.status(400).json({message:"User with this email does not exist"})
        }

        const secret=process.env.JWT_SECRET+user.password

        const token=await jwt.sign({
            email:user.email,
            id:user._id
        },
        secret,
        {
            expiresIn:process.env.JWT_EXPIRY
        })

        const url=`${process.env.FRONTEND_URL}/Teacher/resetpassword/${user._id}/${token}`
        const transporter=nodemailer.createTransport({
            host:'smtp.gmail.com',
            port:587,
            auth:{
                user:process.env.EMAIL,
                pass:process.env.PASSWORD
            }
        })
        await transporter.sendMail({
            to:user.email,
            subject:'Password Reset Link',
            text:`Click on the link below to reset your password
            ${url}`,
            html:`<h2>Click on the link below to reset your password</h2>
            <a href=${url}>Password Reset</a>`
        })
        res.status(200).json({message:"Password reset link has been sent to your email"})
    } catch (error) {
        res.status(500).json({message:"Something went wrong"})
    }
}

const teacherResetPassword=async(req,res)=>{
    try {
        const user=await Teacher.findById(req.params.id)

        if(!user){
            res.status(400).json({message:"User does not exist"})
        }

        const secret=process.env.JWT_SECRET+user.password

        const decoded=await jwt.verify(req.params.token,secret)

        if(decoded.id===user._id.toString()){
            user.password=req.body.password
            const salt=await bcrypt.genSalt(10)
            user.password=await bcrypt.hash(user.password,salt)

            await user.save()
            res.status(200).json({message:"Password reset successful"})
        }
        else{
            res.status(400).json({message:"Invalid token"})
        }
    } catch (error) {
        res.status(500).json({message:"Something went wrong"})
    }
}


